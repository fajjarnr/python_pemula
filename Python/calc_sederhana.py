pengurangan = a - b
print("Hasil 7 - 3 = ", pengurangan)

perkalian = a * b
print("Hasil 7 * 3 = ", perkalian)

pembagian = 7 / 3
print("Hasil 7 / 3 = ", pembagian)

modulo = a % b
print("Hasil 7 % 3 = ", modulo)

pangkat = a ** b
print("Hasil 7 ** 3 = ", pangkat)